/**
 * Clarification API client (T202 + T208 + T602).
 *
 * Wraps the FastAPI clarification endpoints used by the candidate/profile
 * visualisation UI and the employer-side talent-image UI:
 *   - POST /api/clarification/synthesize        → run the multi-step LLM pipeline
 *   - GET  /api/clarification/my-profile        → fetch the persisted record
 *   - POST /api/clarification/synthesize-employer → employer counterpart
 *   - GET  /api/clarification/role/{role_id}    → employer record
 *
 * The shapes below mirror the JSON contract produced by
 * backend/agents/jobseeker/clarifier_agent.py — see the record that agent
 * upserts into `candidate_clarifications` for the field list.
 */

import { createClient } from "@/lib/supabase";

// ---------------------------------------------------------------------------
// Types — must stay in sync with candidate_clarifications table columns
// ---------------------------------------------------------------------------

export type SourceLabel =
  | "profile"
  | "journals"
  | "conversations"
  | "emotion_history"
  | "cv"
  | "agent_inference"
  | "unknown";

export type Priority = "high" | "medium" | "low";

/** A value with provenance metadata — produced by the LLM for each insight. */
export interface AttributedValue<T = string> {
  value: T;
  reasoning?: string;
  confidence?: number;
  sources?: SourceLabel[];
}

/** Conflict between two data sources — what the agent flags in `conflict_flags`. */
export interface Contradiction {
  source_a: string;
  source_b: string;
  explanation: string;
  /** Optional: the actual fields that disagree. */
  fields?: string[];
}

/** A follow-up question generated by the agent to close an info gap. */
export interface FollowUpQuestion {
  question: string;
  priority: Priority;
  purpose: string;
  /** Whether the user has already answered (locally merged from clarifications). */
  answered?: boolean;
  /** When answered, the answer text. */
  answer?: string | null;
}

/** The `profile_synthesis` JSON column. */
export interface ProfileSynthesis {
  summary?: AttributedValue<string>;
  explicit_skills?: AttributedValue[];
  implicit_traits?: AttributedValue[];
  value_orientation?: AttributedValue[];
  career_interests?: AttributedValue[];
  [key: string]: unknown;
}

/** A persisted candidate_clarifications row. */
export interface CandidateClarification {
  id?: string;
  user_id?: string;
  candidate_id?: string | null;
  profile_synthesis?: ProfileSynthesis;
  explicit_needs?: unknown[];
  implicit_needs?: unknown[];
  must_haves?: unknown[];
  nice_to_haves?: unknown[];
  deal_breakers?: unknown[];
  conflict_flags?: Contradiction[];
  follow_up_questions?: FollowUpQuestion[];
  confidence_score?: number;
  info_completeness?: number;
  last_synthesized_at?: string;
  created_at?: string;
  updated_at?: string;
}

/** The return shape of POST /api/clarification/synthesize. */
export interface SynthesizeResponse {
  text: string;
  synthesis: {
    profile_synthesis?: ProfileSynthesis;
    real_needs?: {
      explicit?: unknown[];
      implicit?: unknown[];
      must_haves?: unknown[];
      nice_to_haves?: unknown[];
      deal_breakers?: unknown[];
    };
    contradictions?: Contradiction[];
    follow_up_questions?: FollowUpQuestion[];
    info_completeness?: AttributedValue<number> | number;
    overall_confidence?: number;
    reflection?: {
      issues?: string[];
      corrections?: Record<string, unknown>;
      confidence_after_reflection?: number;
    };
    reasoning_chain?: string[];
  };
}

// ---------------------------------------------------------------------------
// HTTP plumbing
// ---------------------------------------------------------------------------

const API_BASE =
  process.env.NEXT_PUBLIC_API_URL?.replace(/\/$/, "") || "http://localhost:8000";

async function authHeaders(): Promise<HeadersInit> {
  // Prefer supabase session; fall back to the legacy sb_token localStorage
  // entry that the realtime chat stores.
  try {
    const supabase = createClient();
    const { data } = await supabase.auth.getSession();
    const token = data.session?.access_token;
    if (token) return { Authorization: `Bearer ${token}` };
  } catch {
    /* fall through */
  }
  if (typeof window !== "undefined") {
    const legacy = window.localStorage.getItem("sb_token");
    if (legacy) return { Authorization: `Bearer ${legacy}` };
  }
  return {};
}

async function request<T>(
  path: string,
  init: RequestInit = {},
): Promise<T> {
  const headers: Record<string, string> = {
    "Content-Type": "application/json",
    ...((await authHeaders()) as Record<string, string>),
    ...((init.headers as Record<string, string>) ?? {}),
  };

  const res = await fetch(`${API_BASE}${path}`, { ...init, headers });
  if (!res.ok) {
    let body: unknown;
    try {
      body = await res.json();
    } catch {
      body = await res.text();
    }
    throw new Error(
      `Clarification API ${res.status} ${res.statusText}: ${JSON.stringify(body)}`,
    );
  }
  if (res.status === 204) return undefined as T;
  return (await res.json()) as T;
}


/** A single persona's submitted brief / spec snippet — what the consensus
 * engine sees from each side. */
export interface StakeholderStance {
  role: StakeholderRole;
  /** 0..100 — higher means the role's view aligns with the consensus. */
  alignment: number;
  summary?: string;
  /** Optional list of requirements this stakeholder emphasised. */
  emphasis?: string[];
}

/** Talent image dimensions populated by the employer clarifier agent. */
export interface TalentImage {
  hard_skills?: string[];
  soft_skills?: string[];
  experience_profile?: Record<string, string | number>;
  cultural_fit?: Record<string, string>;
  summary?: string;
}

/** A persisted employer_clarifications row. */
export interface EmployerClarification {
  id?: string;
  role_id?: string;
  talent_image?: TalentImage;
  hard_skills?: string[];
  soft_skills?: string[];
  experience_profile?: Record<string, string | number>;
  cultural_fit?: Record<string, string>;
  explicit_requirements?: unknown[];
  implicit_requirements?: unknown[];
  must_haves?: unknown[];
  nice_to_haves?: unknown[];
  contributor_inputs?: {
    brief?: Record<string, unknown>;
    spec?: Record<string, unknown>;
    compliance?: Record<string, unknown>;
    policy?: Record<string, unknown>;
  };
  conflicts?: Contradiction[] | string[];
  consensus_score?: number;
  follow_up_questions?: FollowUpQuestion[];
  last_synthesized_at?: string;
}

/** Best-effort view of one stakeholder's stance, derived from
 * `contributor_inputs`. Each side shows up even when its slot is empty. */
export function deriveStakeholderStances(
  row: EmployerClarification | null,
): StakeholderStance[] {
  if (!row?.contributor_inputs) {
    return [];
  }
  const { brief, spec, compliance, policy } = row.contributor_inputs;
  const has = (x: unknown) => {
    if (!x || typeof x !== "object") return false;
    const o = x as Record<string, unknown>;
    return Object.values(o).some(
      (v) => v != null && !(typeof v === "string" && v.length === 0),
    );
  };
  const baseline = row.consensus_score ?? 0.5;
  return [
    {
      role: "boss",
      summary: describe(brief),
      alignment: scale(has(brief), baseline),
      emphasis: pickEmphasis(brief),
    },
    {
      role: "hr",
      summary: describe({ ...(compliance ?? {}), ...(policy ?? {}) }),
      alignment: scale(has(compliance) || has(policy), baseline),
      emphasis: pickEmphasis({ ...(compliance ?? {}), ...(policy ?? {}) }),
    },
    {
      role: "dept_head",
      summary: describe(spec),
      alignment: scale(has(spec), baseline),
      emphasis: pickEmphasis(spec),
    },
  ];
}

function describe(input: Record<string, unknown> | undefined): string {
  if (!input) return "未提交";
  const summary = (input as Record<string, unknown>).summary;
  if (typeof summary === "string" && summary.trim()) return summary;
  return "已提交";
}

function pickEmphasis(input: Record<string, unknown> | undefined): string[] {
  if (!input) return [];
  const must = (input as Record<string, unknown>).must_haves;
  if (Array.isArray(must)) {
    return must
      .map((m) => (typeof m === "string" ? m : null))
      .filter((m): m is string => Boolean(m))
      .slice(0, 3);
  }
  return [];
}

function scale(hasContent: boolean, baseline: number): number {
  if (!hasContent) return 30;
  // Up to ±20 points around the consensus baseline, clamped to 0..100.
  return Math.max(0, Math.min(100, Math.round((0.6 + baseline * 0.4) * 100)));
}


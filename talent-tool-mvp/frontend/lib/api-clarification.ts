export const clarificationApi = {
  /**
   * Run the multi-step LLM pipeline (synthesize → reflect → persist).
   * Returns the agent's freeform reply + the structured synthesis object.
   */
  synthesize(): Promise<SynthesizeResponse> {
    return request<SynthesizeResponse>("/api/clarification/synthesize", {
      method: "POST",
      body: JSON.stringify({}),
    });
  },

  /** Persisted candidate clarification row (may be empty `{}` if never run). */
  myProfile(): Promise<CandidateClarification> {
    return request<CandidateClarification>("/api/clarification/my-profile", {
      method: "GET",
      cache: "no-store",
    });
  },

  // ----- Employer side (kept here so the same client can drive both) -----

  synthesizeEmployer(body: {
    role_id: string;
    brief?: Record<string, unknown>;
    spec?: Record<string, unknown>;
    compliance?: Record<string, unknown>;
    policy?: Record<string, unknown>;
  }): Promise<{ text: string; summary: Record<string, unknown> }> {
    return request<{ text: string; summary: Record<string, unknown> }>(
      "/api/clarification/synthesize-employer",
      { method: "POST", body: JSON.stringify(body) },
    );
  },

  roleClarification(roleId: string): Promise<Record<string, unknown>> {
    return request<Record<string, unknown>>(
      `/api/clarification/role/${encodeURIComponent(roleId)}`,
      { method: "GET", cache: "no-store" },
    );
  },
};

// ---------------------------------------------------------------------------
// Helpers — coerce the heterogeneous shapes into display-friendly values.
// The LLM may return either plain strings OR `AttributedValue` objects for
// needs fields, so normalise before rendering.
// ---------------------------------------------------------------------------

export type NormalisedItem = {
  text: string;
  reasoning?: string;
  confidence?: number;
  sources?: SourceLabel[];
};

function normaliseOne(item: unknown): NormalisedItem | null {
  if (item == null) return null;
  if (typeof item === "string") return { text: item };
  if (typeof item === "object") {
    const obj = item as Record<string, unknown>;
    const value = (obj.value ?? obj.text ?? obj.label ?? obj.name) as
      | string
      | undefined;
    if (!value) return null;
    return {
      text: String(value),
      reasoning: (obj.reasoning as string | undefined) ?? undefined,
      confidence: typeof obj.confidence === "number" ? obj.confidence : undefined,
      sources: Array.isArray(obj.sources)
        ? (obj.sources as SourceLabel[])
        : undefined,
    };
  }
  return null;
}

export function normaliseList(items: unknown): NormalisedItem[] {
  if (!Array.isArray(items)) return [];
  return items
    .map(normaliseOne)
    .filter((x): x is NormalisedItem => x !== null);
}

